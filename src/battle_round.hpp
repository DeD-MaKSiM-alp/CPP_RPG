#pragma once

// =============================================================================
// battle_round.hpp — сценарий одного полного раунда боя (ГГЖ → союзник → враг).
//
// Зачем отдельно от main.cpp:
//   Точка входа должна только подготовить мир и крутить цикл «пока бой не решён»,
//   а не расписывать каждый полуход. Вся пошаговая оркестрация раунда сосредоточена
//   здесь: ввод меню ГГЖ, вызовы game_rules и battle_io, накопление текстовой сводки.
//
// Зависимости: game_types, game_rules, battle_io — без новых игровых правил.
// =============================================================================

#include "game_types.hpp"

namespace battle_round {

// -----------------------------------------------------------------------------
// RoundOutcomeKind — чем закончился вызов run_single_round для главного цикла.
//
// ContinueBattle: раунд дошёл до конца (сводка «раунд завершён»), внешний while
//   может перейти к следующему номеру раунда.
// PlayerExit: игрок ввёл 0 (или EOF на вводе) — бой прерывается без поражения.
// Victory: враг уничтожен внутри этого раунда (после хода ГГЖ или союзника).
// DefeatAlly: союзник погиб после хода врага.
//
// Почему enum, а не bool: одна точка возврата с ясной семантикой для main.
// -----------------------------------------------------------------------------
enum class RoundOutcomeKind {
  ContinueBattle,
  PlayerExit,
  Victory,
  DefeatAlly
};

// -----------------------------------------------------------------------------
// SingleRoundResult — компактный результат одного вызова run_single_round.
// Расширять полями можно позже (например флаги для тестов), сейчас достаточно kind.
// -----------------------------------------------------------------------------
struct SingleRoundResult {
  RoundOutcomeKind kind{RoundOutcomeKind::ContinueBattle};
};

// -----------------------------------------------------------------------------
// DemoBattleState — стартовое состояние демо-боя (имена, части, числа как в main).
// Собрано в одном месте, чтобы main не раздувался блоками push_back.
// -----------------------------------------------------------------------------
struct DemoBattleState {
  game::GgHeroine gg;
  game::Unit ally;
  game::Unit enemy;
};

// Создаёт то же начальное состояние, что раньше создавалось в main построчно.
DemoBattleState make_demo_battle();

// Выполняет один полный раунд: баннер, три полухода, проверки исхода, сводка.
// round_index — номер раунда для заголовка (в main увеличивается до вызова).
//
// Возвращает, нужно ли продолжать внешний цикл боя (см. RoundOutcomeKind).
SingleRoundResult run_single_round(game::GgHeroine& gg, game::Unit& ally, game::Unit& enemy,
                                   int battle_intensity, int attack_damage, int round_index);

}  // namespace battle_round
