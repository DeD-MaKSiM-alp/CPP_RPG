#pragma once // заголовок подключают из main и потенциально из тестов — защита от дублирования

// =============================================================================
// Файл: battle_round.hpp
// Назначение: сценарий одного полного раунда боя (ход ГГЖ → союзник → враг) и демо-старт.
// Точка входа (main) только крутит цикл «пока бой не решён», без деталей полуходов.
// Зависимости: game_types — данные; game_rules и battle_io подключаются в .cpp.
// =============================================================================

#include "game_types.hpp" // GgHeroine, Unit и всё необходимое для состояния боя

namespace battle_round { // модуль «один раунд и демо-сборка», отдельно от game::

// -----------------------------------------------------------------------------
// RoundOutcomeKind — чем закончился вызов run_single_round для главного цикла.
//
// ContinueBattle: раунд дошёл до конца (сводка «раунд завершён»), внешний while
//   может перейти к следующему номеру раунда.
// PlayerExit: игрок ввёл 0 (или EOF на вводе) — бой прерывается без поражения.
// Victory: враг уничтожен внутри этого раунда (после хода ГГЖ или союзника).
// DefeatAlly: союзник погиб после хода врага.
//
// Почему enum, а не bool: одна точка возврата с ясной семантикой для main.
// -----------------------------------------------------------------------------
enum class RoundOutcomeKind {
  ContinueBattle, // бой продолжается, наружный цикл в main может вызвать следующий раунд
  PlayerExit,     // игрок вышел из игры по меню
  Victory,        // враг убит
  DefeatAlly      // союзник убит
};

// -----------------------------------------------------------------------------
// SingleRoundResult — компактный результат одного вызова run_single_round.
// Расширять полями можно позже (например флаги для тестов), сейчас достаточно kind.
// -----------------------------------------------------------------------------
struct SingleRoundResult {
  RoundOutcomeKind kind{RoundOutcomeKind::ContinueBattle}; // по умолчанию считаем, что раунд просто продолжил бой
};

// -----------------------------------------------------------------------------
// DemoBattleState — стартовое состояние демо-боя (имена, части, числа как в main).
// Собрано в одном месте, чтобы main не раздувался блоками push_back.
// -----------------------------------------------------------------------------
struct DemoBattleState {
  game::GgHeroine gg;   // главная героиня — игрок
  game::Unit ally;      // союзник с автоматическим поведением
  game::Unit enemy;     // противник
};

/**
 * make_demo_battle — создаёт заполненное демо-состояние трёх участников с фиксированными числами.
 * Зачем: вынести длинную инициализацию из main в одну функцию.
 * Где используется: только в main.cpp перед циклом раундов.
 * Параметры: нет.
 * Возвращает: структуру DemoBattleState с именами, HP, Lust, частями тел и порогами.
 */
DemoBattleState make_demo_battle();

/**
 * run_single_round — выполняет один полный раунд: баннер, три полухода, проверки исхода, сводка.
 * Зачем: вся пошаговая оркестрация в одном месте, main остаётся коротким.
 * Где используется: внутри цикла while в main.cpp.
 * Параметры: gg, ally, enemy — изменяемое состояние; battle_intensity, attack_damage — константы боя;
 *            round_index — номер раунда для заголовка (в main увеличивается до вызова).
 * Возвращает: SingleRoundResult с полем kind — нужно ли продолжать внешний цикл боя.
 */
SingleRoundResult run_single_round(game::GgHeroine& gg, game::Unit& ally, game::Unit& enemy,
                                   int battle_intensity, int attack_damage, int round_index);

}  // namespace battle_round
