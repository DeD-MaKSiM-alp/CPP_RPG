namespace game {

// -----------------------------------------------------------------------------
// allied_attack_redirects_to_gg_armor
// Зачем: единое булево правило «срыва» союзника — цель удара не враг, а броня ГГЖ.
// Принимает: ally_lust — текущий Lust союзника на момент ПЕРЕД приростом за этот полуход;
//            lust_redirect_threshold — порог срыва с частей/шаблона союзника.
// Возвращает: true, если удар должен идти по броне ГГЖ (включая ровно порог: >=).
// Почему отдельно от main: то же условие проверяется в тестах без запуска боя.
// -----------------------------------------------------------------------------
inline bool allied_attack_redirects_to_gg_armor(int ally_lust, int lust_redirect_threshold) {
  return ally_lust >= lust_redirect_threshold;
}

}  // namespace game
